// Helper to generate AI price estimate
export function generatePriceEstimate(
  make: string,
  model: string,
  year: number,
  mileage: number
): PriceEstimate {
  // Simple mock calculation
  const basePrice = 25000
  const yearFactor = (year - 2015) * 2000
  const mileagePenalty = mileage * 0.05
  const recommended = Math.round((basePrice + yearFactor - mileagePenalty) / 100) * 100

  return {
    min: Math.round(recommended * 0.9),
    recommended,
    max: Math.round(recommended * 1.15),
    confidence: 0.75 + Math.random() * 0.15,
  }
}

